#include "quaternion.h"

void	normalize_quat(float q[4])
{
	int		i;
	float	mag;

	mag = sqrt(q[0] * q[0] + q[1] * q[1] + q[2] * q[2] + q[3] * q[3]);
	i = 0;
	while (i < 4)
	{
		q[i] /= mag;
		i++;
	}
}
